//Problem 2: Check if a number is even or odd....
// •  "Even" agar number 2 se divide ho jaye bina remainder ke (num % 2 == 0).
// •  "Odd" agar number 2 se divide karne par remainder aaye (num % 2 != 0).
//************************** END OF STATEMENT ****************************

let num = 7;

if (num % 2 == 0) {
  console.log("Even");
} else {
  console.log("Odd");
};
